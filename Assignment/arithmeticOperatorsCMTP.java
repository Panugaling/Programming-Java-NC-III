package Assignment; // The package name

// This program demonstrates basic arithmetic operations in Java
// It performs addition, subtraction, multiplication, and division on two floating-point numbers

public class arithmeticOperatorsCMTP { // The class name 'arithmeticOperatorsCMTP' should match the file name
    public static void main(String[] args) { // Main method where execution starts
        double num1 = 20.5, num2 = 5.2; // Declaring and initializing two floating-point numbers

        // Performing arithmetic operations and displaying results
        System.out.println("Addition: " + (num1 + num2)); // Adds num1 and num2
        System.out.println("Subtraction: " + (num1 - num2)); // Subtracts num2 from num1
        System.out.println("Multiplication: " + (num1 * num2)); // Multiplies num1 by num2
        System.out.println("Division: " + (num1 / num2)); // Divides num1 by num2
    }
}
